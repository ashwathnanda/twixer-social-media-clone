const {model, Schema} = require('mongoose')

//Mongo Schema for Posts

const postsSchema = new Schema({
    body: String,
    username: String,
    createdAt: String,
    comments: [
        {
            body: String,
            username: String,
            createdAt: String
        }
    ],
    likes: [
        {
            username: String,
            createdAt: String
        }
    ],
    user: {
        type: Schema.Types.ObjectId,
        ref: 'Users'
    }
})

module.exports = model("Posts", postsSchema)